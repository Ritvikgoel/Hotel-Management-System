    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        String usrnme=jTextField1.getText();
        String psswrd=jPasswordField1.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver"); //register driver class
            Connection con;
            con=DriverManager.getConnection("jdbc:mysql://localhost:3307/project","root","");
            Statement stmt=con.createStatement();
            String query="Select password from login where username='"+usrnme+"';";
            ResultSet rs=stmt.executeQuery(query);
            while(rs.next())
            {
                String pwd=rs.getString("Password");
                if(psswrd.equals(pwd))
                {
                    JOptionPane.showMessageDialog(null,"Logged In Successfully");
                    this.setVisible(false);
                    new MainMenu().setVisible(true);
                }
                else
                {JOptionPane.showMessageDialog(null,"Invaild Details");}
            }
            rs.close();
            stmt.close();
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Connectivity error");
        }
    }//GEN-LAST:event_LoginActionPerformed

